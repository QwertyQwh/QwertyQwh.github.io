<p className='quote'>About a dozen new teachers of criminal law at multiple institutions have told me that they are not including rape law in their courses, arguing that it’s not worth the risk of complaints of discomfort by students. Even seasoned teachers of criminal law, at law schools across the country, have confided that they are seriously considering dropping rape law and other topics related to sex and gender violence.</p>

